package network;

public class User{
private int source;
private int target;
private float weight;
public void setSource(int source) {
	this.source = source;
}
public void setTarget(int target) {
	this.target = target;
}
public void setWeight(float weight) {
	this.weight = weight;
}
public int getSource() {
	return source;
}
public int getTarget() {
	return target;
}
public float getWeight() {
	return weight;
}
}
